# Crie um dicionário pessoa com as chaves "nome", "cidade" e "profissao".

# Imprima todas as chaves usando .keys() e todos os valores usando .values(), cada um em uma linha separada.

pessoa = {
    "nome":"brenda",
    "cidade":"sao paulo",
    "profissao":"eng de dados"
}

print("Chaves: ", pessoa.keys())
print("Valores: ", pessoa.values())